const int N = 1e5 + 10;
vi adj[N];
int par[N], sz[N];
int head[N], id[N], pos[N], tour[N];
int cur_pos = 1, cur_chain = 0;
void dfs(int u, int p = -1) {
    sz[u] = 1;
    for (int v : adj[u]) if (v != p) {
        par[v] = u;
        dfs(v, u);
        sz[u] += sz[v];
    }
}
void hld(int u, int p = -1) {
    if (!head[cur_chain]) head[cur_chain] = u;
    id[u] = cur_chain;
    pos[u] = cur_pos;
    tour[cur_pos++] = u;
    int nxt = 0;
    for (int v : adj[u]) if (v != p && (nxt == 0 || sz[v] > sz[nxt])) nxt = v;
    if (nxt) hld(nxt, u);
    for (int v : adj[u]) if (v != p && v != nxt) cur_chain++, hld(v, u);
}
int lca(int u, int v) {
    while (id[u] != id[v]) {
        if (pos[u] < pos[v]) swap(u, v);
        u = par[head[id[u]]];
    }
    return (pos[u] < pos[v]) ? u : v;
}
