const int N = 1e5 + 10;
struct Edge {
    int u, v, w;
}; vector<Edge> a;
int n, m;
struct dsu {
    int par[N], sz[N];
    void make_set(int u) {
        par[u] = u;
        sz[u] = 1;
    }
    void init(int n) {
        for (int i = 1; i <= n; i++) make_set(i);
    }
    int Find(int u) {
        return par[u] == u ? u : par[u] = Find(par[u]);
    }
    bool same(int u, int v) {
        return Find(u) == Find(v);
    }
    bool Merge(int u, int v) {
        u = Find(u);
        v = Find(v);
        if (u == v) return false;
        if (sz[u] < sz[v]) swap(u, v);
        par[v] = u;
        sz[u] += sz[v];
        return true;
    }
} dsu;
