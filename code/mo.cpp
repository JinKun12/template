const int N = 1e5 + 10;
const int S = 447;
int n, m, a[N], cnt[N / S][N];
int res[N], dem[N], curRes = 0;
struct query {
    int l, r, id;
} qs[N];
void check(int x, int delta) {
    if (x > n) return;
    dem[x] += delta;
    if (dem[x] == x) ++curRes;
    if (dem[x] == x + delta) --curRes;
}
void add(int l, int r) {
    for (int i = l; i <= r; i++)
        check(a[i], 1);
}
void del(int l, int r) {
    for (int i = l; i <= r; i++)
        check(a[i], -1);
}
void calc(int i) {
    int l = qs[i].l, r = qs[i].r;
    int _l = qs[i - 1].l, _r = qs[i - 1].r;
    if (_l < l) del(_l, l - 1);
    if (l < _l) add(l, _l - 1);
    if (_r < r) add(_r + 1, r);
    if (r < _r) del(r + 1, _r);
}
Nyaa() {
    cin.tie(nullptr)->sync_with_stdio(false);
//    freopen(Task".inp", "r", stdin);
//    freopen(Task".out", "w", stdout);
    cin >> n >> m;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        if (a[i] <= n)
            ++cnt[i / S][a[i]];
    }
    for (int i = 1; i <= m; i++) {
        auto &[l, r, id] = qs[i];
        cin >> l >> r; id = i;
    }
    sort(qs + 1, qs + m + 1, [](query p, query q) {
        if (p.l / S != q.l / S) return p.l < q.l;
        return p.r < q.r;
    });
    int curL = qs[1].l, curR = qs[1].r;
    for (int i = curL; i <= curR; i++)
        check(a[i], 1);
    res[qs[1].id] = curRes;
    for (int i = 2; i <= m; i++) {
        calc(i);
        res[qs[i].id] = curRes;
    }
    for (int i = 1; i <= m; i++)
        cout << res[i] << '\n';
    cerr << "\nTime elapsed: " << 1000.0 * clock() / CLOCKS_PER_SEC << " ms.\n";
    return 0;
}
