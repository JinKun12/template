const int N = 1e5 + 10;
int n, m; vi adj[N];
int cnt, tim[N], tour[N << 1];
int T[N << 2];
void dfs(int u, int p = -1) {
    tim[u] = ++cnt;
    tour[cnt] = u;
    for (int v : adj[u]) {
        if (v != p) {
            dfs(v, u);
            tour[++cnt] = u;
        }
    }
}
int mn_tim(int x, int y) {
    if (x == -1) return y;
    if (y == -1) return x;
    return (tim[x] < tim[y] ? x : y);
}
void init(int node, int l, int r) {
    if (l == r) {
        T[node] = tour[l];
        return;
    }
    int mid = (l + r) >> 1;
    init(node << 1, l, mid);
    init(node << 1 | 1, mid + 1, r);
    T[node] = mn_tim(T[node << 1], T[node << 1 | 1]);
}
int get(int node, int l, int r, int u, int v) {
    if (u > r || v < l) return -1;
    if (u <= l && r <= v) return T[node];
    int mid = (l + r) >> 1;
    return mn_tim(get(node << 1, l, mid, u, v),
                  get(node << 1 | 1, mid + 1, r, u, v));
}
int lca(int a, int b) {
    if (tim[a] > tim[b]) swap(a, b);
    return get(1, 1, cnt, tim[a], tim[b]);
}
