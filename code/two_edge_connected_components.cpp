const int N = 2e5 + 10;
int n, m; vi adj[N];
int cnt = 0, scc = 0;
int id[N], low[N];
vi ans[N];
stack<int> st;
void dfs(int u, int p = -1) {
    id[u] = low[u] = ++cnt;
    st.push(u);
//    bool flag = false;
    for (int v : adj[u]) {
//        if (v == p && !flag) {
//            flag = true;
//            continue;
//        }
        if (id[v]) cmin(low[u], id[v]);
        else {
            dfs(v, u);
            cmin(low[u], low[v]);
        }
    }
    if (low[u] == id[u]) {
        while (st.top() != u) {
            ans[scc].pb(st.top());
            st.pop();
        }
        ans[scc++].pb(st.top());
        st.pop();
    }
}
